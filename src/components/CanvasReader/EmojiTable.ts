let gReverseLookup: StashOf<string> | undefined;

export function lookupEmoji(char: string) {
  if (!gReverseLookup) {
    gReverseLookup = {};
    for (const group in EMOJI_TABLE) {
      for (const name in EMOJI_TABLE[group]) {
        gReverseLookup[EMOJI_TABLE[group][name]] = `${group}:${name}`;
      }
    }
  }
  return gReverseLookup[char];
}
